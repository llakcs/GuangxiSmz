package com.th.guangxismz.Bean;

public class HashResult {

    /**
     * emp_count : 321
     * sha1 : 04c09cf9055144ca0526f5691c2c987b
     * crc16 : 52057
     */

    private int emp_count;
    private String sha1;
    private String crc16;

    public int getEmp_count() {
        return emp_count;
    }

    public void setEmp_count(int emp_count) {
        this.emp_count = emp_count;
    }

    public String getSha1() {
        return sha1;
    }

    public void setSha1(String sha1) {
        this.sha1 = sha1;
    }

    public String getCrc16() {
        return crc16;
    }

    public void setCrc16(String crc16) {
        this.crc16 = crc16;
    }
}
